void solve() {
    int t;
    cin >> t;
    while (t--) {
        int n, k;
        cin >> n >> k;
        int arr[n];
        set<int>s;
        int sum = 0;
        for (int i = 0; i < n; i++) {cin >> arr[i]; s.insert(arr[i]); sum += arr[i];}
        sort(arr, arr + n);
        if (sum == k ) {cout << "NO" << nline; continue;}

        sum = 0;
        vector<int>res(n);
        for (int i = 0; i < n; i++) {
            sum += arr[i];
            if (sum == k) {
                swap(arr[i], arr[n - 1]);
            }
            res[i] = arr[i];
        }
        cout << "YES\n";
        for (auto i : res)cout << i << " " ;
        cout << nline;

    }
}
